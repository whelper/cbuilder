    <p class="sbl_txt">과학정책 추진동향</p>
    <p class="bold mt20">기본방향 및 전략</p>
    <ul class="bul">
        <li style="font-weight:300;">
        에너지, 광업, 원자재 가공 부문에서 선도적 지위를 유지하며 첨단 지식 기반 경제를 건설하는 것을 주요 추진<br />
        <p class="ldot">자원주도형 → 혁신주도형 성장모델로의 전환 추진</p>
        </li>
        <li>
        기술 강국 복귀를 위해 우선분야 선정<br />
        <p class="ldot">제약, 첨단 화학, 비금속 및 복합재료, 항공, 정보통신기술, 나노 기술 분야</p>
        <p class="ldot">통적 우위 산업인 원자력, 우주 산업도 지속 발전이 필요하며, 우선분야는 세계시장 환경 변화에 따라 조정</p>
        </li>
        <li>
        범부처 프로그램인 “2020년까지 러시아 연방 장기 사회・경제 발전 개념”(’08.11)을 바탕으로 “2020년까지의 러시아 연방 과학혁신 발전전략” 수립(’11.12)<br />
        <p class="ldot">2020년까지 GDP 대비 연구개발 투자를 2.5~3%로 확대하고, 민간 부문의 투자를 절반 이상으로 증대</p>
        <p class="ldot">러시아 과학자의 논문 발표 건수를 2020년까지 세계 과학저널 전체 발표 건수의 3%까지 확대(2010년 기준 2.08%)</p>
        <p class="ldot">전체 연구개발 지원 규모에서 대학에 대한 지원 비중을 25%까지 확대 등</p>
        </li>
        <li class="bul_bl">
        <span class="blue">2020년까지 과학기술 발전에 1조 5천억 루블 지원(‘12.12)</span><br />
        <p class="ldot">“과학기술발전” 프로그램을 통해 2020년까지 과학기술분야에 1조 5천억 루블 지원. 프로그램에 계획된 지원금은 1조 5천억 루블을 상회</p>
        </li>
    </ul>

